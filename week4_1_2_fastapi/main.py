# 定义事件监听器并缓存事件数据
async def event_listener():
    print("事件监听已启动！")
    filter_topics = contract.events.Transfer.get_filter_topics()
    # lastEpoch = web3.cfx.epoch_number # latest_mined epoch number
    lastEpoch = web3.cfx.epoch_number_by_tag(
        "latest_state")  # 从当下的最新executed的epochNumber开始
    while True:
        new_logs = web3.cfx.get_logs(
            fromEpoch=lastEpoch, topics=filter_topics)
        # new_logs = web3.cfx.get_logs(
        # 记录下这次的epoch 作为lastEpoch 下次查询从这个epoch开始查询
        lastEpoch = web3.cfx.epoch_number_by_tag("latest_state")
        print(new_logs)
        # 在这里执行缓存事件数据的操作
        # print("缓存事件：", event_data)
        for new_log in new_logs:
            print("缓存事件：", new_log)

        await asyncio.sleep(2)


# 在FastAPI应用程序启动时启动事件监听器
@app.on_event("startup")
async def startup_event():
    # 启动事件监听器任务
    asyncio.create_task(event_listener())
